    private void jButtonAceptarIniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarIniciarSesionActionPerformed
        try {
            if (jTextFieldCorreoIS.getText().equals("") || jPasswordFieldIS.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Rellene los campos de texto.", "Inicio de sesión", JOptionPane.ERROR_MESSAGE);
            } else {
                if (JavaBNB.iniciarSesion(jTextFieldCorreoIS.getText(), jPasswordFieldIS.getText())) {
                    jPasswordFieldIS.setText("");
                    if (jTextFieldCorreoIS.getText().equals(Administrador.getCorreoAdm())) {
                        VentanaAdministrador adm = new VentanaAdministrador(this);
                    } else if (JavaBNB.tipoCliente(jTextFieldCorreoIS.getText()).equals("Particular")) {
                        Particular p = (Particular) JavaBNB.buscarClientePorCorreo(jTextFieldCorreoIS.getText());
                        VentanaParticular par = new VentanaParticular(this,p);
                    } else if (JavaBNB.tipoCliente(jTextFieldCorreoIS.getText()).equals("Anfitrion")) {
                        Anfitrion a = (Anfitrion) JavaBNB.buscarClientePorCorreo(jTextFieldCorreoIS.getText());
                        VentanaAnfitrion anf = new VentanaAnfitrion(this,a);
                    }
                }
            }
        } catch (InicioSesionException ise) {
            JOptionPane.showMessageDialog(this, ise.getMessage(), "Inicio de sesión", JOptionPane.ERROR_MESSAGE);
        }catch (NullPointerException npe) {
            JOptionPane.showMessageDialog(this, npe.getMessage(), "Inicio de sesión", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Inicio de sesión", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAceptarIniciarSesionActionPerformed

