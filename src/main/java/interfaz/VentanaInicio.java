    private void jButtonAceptarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarRegistroActionPerformed
        try {
            //leer datos
            String tipoSel = (String) jComboBoxTipo.getSelectedItem();
            String nombre = jTextFieldNombre.getText();
            String dni = jFormattedTextFieldDni.getText();
            String telf = jFormattedTextFieldTelf.getText();
            String correo = jTextFieldCorreoReg.getText();
            String clave1 = jPasswordFieldReg1.getText();
            String clave2 = jPasswordFieldReg2.getText();
            if (tipoSel.equals("Anfitrión")) {
                JavaBNB.registrarse( tipoSel,  nombre,  dni,  telf,  correo,  clave1,  clave2);
            } else if (tipoSel.equals("Particular")) {
                String fechaCaducidad = jFormattedTextFieldFecha.getText();
                String numTarjeta = jFormattedTextFieldNumTarjeta.getText();
                JavaBNB.registrarse( tipoSel,  nombre,  dni,  telf,  correo,  clave1,  clave2, vipSelection(),  fechaCaducidad,  numTarjeta);
            }
            JOptionPane.showMessageDialog(this, "Registrado correctamente. Ahora inice sesión.", "Registro", JOptionPane.INFORMATION_MESSAGE);
            jPanelRegistro.setVisible(false);
            jPanelIniciarSesion.setVisible(true);
        }catch (ClienteRegistradoException | ClaveException | FechaException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Registro", JOptionPane.ERROR_MESSAGE);
        }catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Registro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAceptarRegistroActionPerformed

