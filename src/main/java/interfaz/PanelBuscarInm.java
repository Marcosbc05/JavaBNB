    private void mostrarInmuebles(ArrayList<Inmueble> inmuebles,LocalDate fechaEntrada,LocalDate fechaSalida) {
        try {
            jPanel1.removeAll();
            if (inmuebles.isEmpty()) {
                JLabel jt = new javax.swing.JLabel();
                jt.setText("No se encuentran inmuebles para esa dirección");
                jt.setFont(new java.awt.Font("Segoe UI", 0, 16));
                jPanel1.add(jt);
            } else {
                for (Inmueble i : inmuebles) {
                    PanelInmVistaPrevia p = new PanelInmVistaPrevia(i,parent, fechaEntrada, fechaSalida,true,particular);
                    p.setSize(206, 303);
                    jPanel1.add(p);
                }
            }
            jPanel1.revalidate();
            jPanel1.repaint();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Búsqueda de inmueble", JOptionPane.ERROR_MESSAGE);
        }
    }

