private String modo="Relevancia";
    private void jButtonAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAceptarActionPerformed
        try {
            String localidad = jTextFieldLocalidad.getText();
            String fechaIn = jFormattedTextFechaIn.getText();
            String fechaOut = jFormattedTextFechaOut.getText();
            if (localidad.equals("")||fechaIn.equals("  /  /    ")||fechaOut.equals("  /  /    ")) throw new NullPointerException();
            if (fechaIn.equals(fechaOut)) throw new FechaException("La fecha de entrada y de salida deben ser distintas");
            LocalDate fechaEntrada = JavaBNB.setFechaFutura(fechaIn);
            LocalDate fechaSalida = JavaBNB.setFechaFutura(fechaOut);
            if(fechaEntrada.isAfter(fechaSalida)) throw new FechaException("La fecha de salida debe ser posterior a la de entrada");
            ArrayList<Inmueble> inmuebles = new ArrayList<>();
            switch (modo) {
                case "Relevancia" -> {
                    inmuebles = particular.buscarInmuebleOrdenRelevancia(localidad);
                    break;
                }
                case "Precio" -> {
                    inmuebles = particular.buscarInmuebleOrdenPrecio(localidad);
                    break;
                }
                case "Tipo :" -> {
                    String tipo = (String) jComboBoxTipo.getSelectedItem();
                    inmuebles = particular.buscarInmuebleOrdenTipo(localidad, tipo);
                    break;
                }
            }
            mostrarInmuebles(inmuebles,fechaEntrada,fechaSalida);
            //jComboBoxModoOrdenacionActionPerformed(evt);
        } catch (NullPointerException e) {
            JOptionPane.showMessageDialog(this, "Hay elementos vacíos", "Búsqueda de inmueble", JOptionPane.ERROR_MESSAGE);
        }catch (FechaException e) {
            JOptionPane.showMessageDialog(this, e.getMessage()+"\nModifique las fechas", "Búsqueda de inmueble", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Búsqueda de inmueble", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonAceptarActionPerformed

